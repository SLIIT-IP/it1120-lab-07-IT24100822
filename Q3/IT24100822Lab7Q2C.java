public class IT24100822Lab7Q2C {
    public static void main(String[] args) {
        // Loop for rows
        for (int i = 5; i >= 1; i--) {
            // Loop for printing the numbers
            for (int j = 1; j <= i; j++) {
                System.out.print(i);
            }
            System.out.println(); // Move to the next line
        }
    }
}